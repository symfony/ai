namespace Symfony\Component\Config\Definition\Configurator;

use Symfony\Component\Config\Definition\Builder\ArrayNodeDefinition;

return (new ArrayNodeDefinition('workflow'))
    ->useAttributeAsKey('name')
    ->arrayPrototype()
        ->children()
            ->scalarNode('workflow')
                ->isRequired()
                ->cannotBeEmpty()
                ->info('Name of an existing Symfony Workflow (registered as "workflow.<name>" service)')
            ->end()
            ->arrayNode('executors')
                ->isRequired()
                ->requiresAtLeastOneElement()
                ->useAttributeAsKey('place')
                ->arrayPrototype()
                    ->children()
                        ->enumNode('type')
                            ->values(['agent', 'service'])
                            ->isRequired()
                        ->end()
                        ->scalarNode('agent')
                            ->info('Agent name (references ai.agent.<name>) — required for type: agent')
                            ->cannotBeEmpty()
                        ->end()
                        ->scalarNode('input_key')
                            ->defaultValue('input')
                        ->end()
                        ->scalarNode('output_key')
                            ->defaultValue('output')
                        ->end()
                        ->scalarNode('metadata_key')
                            ->defaultNull()
                            ->info('State key the agent result metadata is written to (for type: agent)')
                        ->end()
                        ->arrayNode('options')
                            ->info('Static options passed to the agent on every call (for type: agent)')
                            ->normalizeKeys(false)
                            ->variablePrototype()->end()
                        ->end()
                        ->scalarNode('options_key')
                            ->defaultNull()
                            ->info('State key holding extra options merged into the agent call (for type: agent)')
                        ->end()
                        ->scalarNode('history_key')
                            ->defaultNull()
                            ->info('State key the conversation MessageBag is accumulated under across places (for type: agent)')
                        ->end()
                        ->scalarNode('service')
                            ->info('Service ID implementing ExecutorInterface — required for type: service')
                            ->cannotBeEmpty()
                        ->end()
                        ->integerNode('timeout')
                            ->defaultValue(0)
                            ->min(0)
                            ->info('Per-place execution timeout in seconds (0 disables it); requires the pcntl extension')
                        ->end()
                        ->arrayNode('retry')
                            ->info('Wrap this executor with automatic retries on failure')
                            ->canBeEnabled()
                            ->children()
                                ->integerNode('max_attempts')
                                    ->defaultValue(3)
                                    ->min(1)
                                ->end()
                                ->integerNode('base_delay_ms')
                                    ->defaultValue(1000)
                                    ->min(0)
                                ->end()
                                ->enumNode('strategy')
                                    ->values(['fixed', 'exponential'])
                                    ->defaultValue('exponential')
                                ->end()
                            ->end()
                        ->end()
                    ->end()
                    ->validate()
                        ->ifTrue(static fn (array $v): bool => 'agent' === $v['type'] && !isset($v['agent']))
                        ->thenInvalid('Executor type "agent" requires the "agent" option.')
                    ->end()
                    ->validate()
                        ->ifTrue(static fn (array $v): bool => 'service' === $v['type'] && !isset($v['service']))
                        ->thenInvalid('Executor type "service" requires the "service" option.')
                    ->end()
                ->end()
            ->end()
            ->arrayNode('store')
                ->addDefaultsIfNotSet()
                ->children()
                    ->enumNode('type')
                        ->values(['memory', 'cache', 'filesystem', 'redis', 'service'])
                        ->defaultValue('memory')
                    ->end()
                    ->scalarNode('cache_service')
                        ->info('PSR-6 cache pool service ID (for type: cache)')
                        ->defaultValue('cache.app')
                    ->end()
                    ->scalarNode('prefix')
                        ->info('Key prefix for cache/redis stores')
                        ->defaultValue('_workflow_state_')
                    ->end()
                    ->integerNode('ttl')
                        ->info('TTL in seconds (for type: cache)')
                        ->defaultValue(86400)
                    ->end()
                    ->scalarNode('directory')
                        ->info('Directory path (for type: filesystem)')
                    ->end()
                    ->scalarNode('redis_client')
                        ->info('Redis client service ID (for type: redis)')
                    ->end()
                    ->scalarNode('service')
                        ->info('Custom service ID implementing WorkflowStateStoreInterface (for type: service)')
                    ->end()
                ->end()
                ->validate()
                    ->ifTrue(static fn (array $v): bool => 'filesystem' === $v['type'] && !isset($v['directory']))
                    ->thenInvalid('Store type "filesystem" requires the "directory" option.')
                ->end()
                ->validate()
                    ->ifTrue(static fn (array $v): bool => 'redis' === $v['type'] && !isset($v['redis_client']))
                    ->thenInvalid('Store type "redis" requires the "redis_client" option.')
                ->end()
                ->validate()
                    ->ifTrue(static fn (array $v): bool => 'service' === $v['type'] && !isset($v['service']))
                    ->thenInvalid('Store type "service" requires the "service" option.')
                ->end()
            ->end()
            ->arrayNode('guards')
                ->info('Service IDs implementing GuardInterface; each guard decides which places it applies to')
                ->scalarPrototype()->cannotBeEmpty()->end()
            ->end()
            ->scalarNode('transition_resolver')
                ->defaultNull()
                ->info('Service ID of a custom TransitionResolverInterface, or null for default')
            ->end()
            ->arrayNode('transition_expressions')
                ->info('Map of transition name to a boolean expression; mutually exclusive with transition_resolver')
                ->useAttributeAsKey('transition')
                ->scalarPrototype()->cannotBeEmpty()->end()
            ->end()
            ->arrayNode('lock')
                ->info('Concurrency-safe locking around run() and resume()')
                ->canBeEnabled()
                ->children()
                    ->scalarNode('factory')
                        ->defaultValue('lock.factory')
                        ->info('Service ID of a Symfony LockFactory')
                        ->cannotBeEmpty()
                    ->end()
                ->end()
            ->end()
            ->arrayNode('parallel')
                ->info('Parallel execution settings for workflows with AND-split branches')
                ->addDefaultsIfNotSet()
                ->children()
                    ->enumNode('merge_policy')
                        ->values(['fail_on_conflict', 'last_branch_wins', 'first_branch_wins', 'prefer_non_null'])
                        ->defaultValue('fail_on_conflict')
                        ->info('How concurrent branch state writes are reconciled when they touch the same key')
                    ->end()
                    ->scalarNode('strategy')
                        ->defaultNull()
                        ->info('Service ID of a custom ParallelExecutionStrategyInterface (defaults to concurrent execution)')
                    ->end()
                ->end()
            ->end()
            ->integerNode('max_steps')
                ->defaultValue(100)
                ->min(1)
                ->info('Maximum number of execution steps before the workflow aborts (guards against cyclic definitions)')
            ->end()
        ->end()
        ->validate()
            ->ifTrue(static fn (array $v): bool => null !== $v['transition_resolver'] && [] !== $v['transition_expressions'])
            ->thenInvalid('The "transition_resolver" and "transition_expressions" options are mutually exclusive.')
        ->end()
    ->end();
